    private void DosyaOkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DosyaOkuActionPerformed
        // TODO add your handling code here:
        int temp = 0;
        JFileChooser fileroot = new JFileChooser();
        int i = fileroot.showOpenDialog(null);
        if (i == JFileChooser.APPROVE_OPTION) {
            try {
                BufferedReader reader = null;
                reader = new BufferedReader(new FileReader(fileroot.getSelectedFile()));
                line = reader.readLine();
                while (line != null) {
                    if (temp == 0) {
                        linkedList1.add(line);
                        linkedList1.NameOfFile = "list1.txt";
                        linkedList1.Addtofile();
                        linkedList1.printList();
                    } else if (temp == 1) {
                        linkedList2.add(line);
                        linkedList2.NameOfFile = "list2.txt";
                        linkedList2.Addtofile();
                        linkedList2.printList();
                    }//else //x=Integer.parseInt(line);
                    temp++;
                    Deneklemler.append(line + "\n");
                    line = reader.readLine();

                }
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Test.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Test.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_DosyaOkuActionPerformed

