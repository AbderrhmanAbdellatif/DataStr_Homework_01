    private void islemcalistirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_islemcalistirActionPerformed
        try {
            if (Aramayap.isSelected()) {
                String gelenKelime[];
                boolean kelimekonturlu = true;
                if (AranacakKelime.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Lutfen aranacak kelime secin  ");
                } else {
                    NodeLL temp = linkedlist.head;
                    for (int i = 0; i < bst.length; i++) {
                        bst[i].inorder();
                        gelenKelime = bst[i].Nodeler.split(" ");
                        for (int j = 0; j < gelenKelime.length; j++) {
                            if (gelenKelime[j].equals(AranacakKelime.getText())) {
                                show.setText(null);
                                show.append("Aranan Kelime  : " + gelenKelime[j]);
                                show.append("\n");
                                File f = new File(temp.value.toString());
                                show.append("dosya adi : " + f.getName());
                                kelimekonturlu = false;
                            }
                        }
                        temp = temp.next;
                    }
                    if (kelimekonturlu) {
                        show.setText(null);
                        JOptionPane.showMessageDialog(this, "boyle bir kelime bulunmadi! ");
                    }
                }
            }
            if (Listele.isSelected()) {
                show.setText(null);
                NodeLL temp = linkedlist.head;
                for (int i = 0; i < bst.length; i++) {
                    bst[i].inorder();
                    File f = new File(temp.value.toString());
                    show.append(f.getName());
                    show.append("\n ");
                    show.append(bst[i].Nodeler);
                    show.append("\n ");
                    temp = temp.next;
                }
            }
            if (Cikis.isSelected()) {
                this.setVisible(false);
            }
        } catch (java.lang.NullPointerException rx) {
            JOptionPane.showMessageDialog(this, "Lutfen dosya konumu sectiniz ");
        }
    }//GEN-LAST:event_islemcalistirActionPerformed
    public static File[] finder(String dirName) {
        File dir = new File(dirName);
        return dir.listFiles(new FilenameFilter() {
            @Override
            public boolean accept(File dir, String filename) {
                return filename.endsWith(".txt");
            }
        });
    }

