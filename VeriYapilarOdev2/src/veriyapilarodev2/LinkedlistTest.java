        path.setText(Aderss);
        dosya = finder(path.getText());
        for (int i = dosya.length - 1; i >= 0; i--) {
            linkedlist.addFirst(dosya[i]);
        }
        bst = new BST[linkedlist.size()];
        NodeLL temp = linkedlist.head;
